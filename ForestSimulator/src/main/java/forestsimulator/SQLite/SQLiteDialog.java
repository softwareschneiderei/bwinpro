        if (jTabbedPane1.getSelectedIndex()==0) st=jpd.createStand();
        if (jTabbedPane1.getSelectedIndex()==1) st=jpp.createStand();
        if (jTabbedPane1.getSelectedIndex()==2) st=jp.createStand();
        st.status=0;
        dispose();
