package treegross.base.thinning;

public class ThinningDefinitions {

    public final String moderateThinning;
    public final String thinningType;
    public final String thinningIntensity;

    public ThinningDefinitions(String moderateThinning, String thinningType, String thinningIntensity) {
        this.moderateThinning = moderateThinning;
        this.thinningType = thinningType;
        this.thinningIntensity = thinningIntensity;
    }
}
